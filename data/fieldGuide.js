// ── Unlock helper ─────────────────────────────────────────────
export function isUnlocked(species) {
  if (species.alwaysUnlocked) return true;
  try {
    const raw = localStorage.getItem(`cast:v1:adventure:${species.unlockedBy}`);
    if (!raw) return false;
    return JSON.parse(raw).completed === true;
  } catch { return false; }
}

